/**
 * Print HTML with meta information for current post: categories, tags, permalink, author, and date.
 *
 * Create your own sowa_entry_meta() to override in a child theme.
 *
 * @since Twenty Thirteen 1.0
 */
function sowa_entry_meta() {
	if ( is_sticky() && is_home() && ! is_paged() )
		echo '<span class="featured-post">' . __( 'Sticky', 'sowa' ) . '</span>';

	if ( ! has_post_format( 'link' ) && 'post' == get_post_type() )
		sowa_entry_date();

	// Translators: used between list items, there is a space after the comma.
	$categories_list = get_the_category_list( __( ', ', 'sowa' ) );
	if ( $categories_list ) {
		echo '<span class="categories-links">' . $categories_list . '</span>';
	}

	// Translators: used between list items, there is a space after the comma.
	$tag_list = get_the_tag_list( '', __( ', ', 'sowa' ) );
	if ( $tag_list ) {
		echo '<span class="tags-links">' . $tag_list . '</span>';
	}

	// Post author
	if ( 'post' == get_post_type() ) {
		printf( '<span class="author vcard"><a class="url fn n" href="%1$s" title="%2$s" rel="author">%3$s</a></span>',
			esc_url( get_author_posts_url( get_the_author_meta( 'ID' ) ) ),
			esc_attr( sprintf( __( 'View all posts by %s', 'sowa' ), get_the_author() ) ),
			get_the_author()
		);
	}
}
